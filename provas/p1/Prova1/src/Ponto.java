public class Ponto {
    private float x;
    private float y;

    public Ponto(float x1, float y1){
        this.x = x1;
        this.y = y1;
    }
    public float getX() {
        return x;
    }
    public void setX(float x) {
        this.x = x;
    }
    public float getY() {
        return y;
    }
    public void setY(float y) {
        this.y = y;
    }
}
